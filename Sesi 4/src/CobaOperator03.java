package Latihan03.sesi4.bin;

public class CobaOperator03{
	public static void main(String[] petrus){
		int c=3;
		c+=7;
		System.out.println("Soal yang menggunakan += [c+=7]");
		System.out.println("hasilnya adalah :"+c);
		System.out.println("");
		System.out.println("");
		int d=5;
		d-=4;
		System.out.println("Soal yang menggunakan -= [d-=4]");
		System.out.println("hasilnya adalah :"+d);
		System.out.println("");
		System.out.println("");
		int e=4;
		e*=5;
		System.out.println("Soal yang menggunakan *= [e*=5]");
		System.out.println("hasilnya adalah :"+e);
		System.out.println("");
		System.out.println("");
		float f=6;
		f/=5;
		System.out.println("Soal yang menggunakan /= [f/=5]");
		System.out.println("hasilnya adalah :"+f);
		System.out.println("");
		System.out.println("");
		int g=12;
		g%=7;
		System.out.println("Soal yang menggunakan %= [g%=7]");
		System.out.println("hasilnya adalah :"+g);
		System.out.println("");
		System.out.println("");	
	}
}